    private void btn_abrebancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_abrebancoActionPerformed
        if (dao.CriaConexao()) {
            if (dao.ConsultarTodos()) {
                bancoLigado = true;
                try {
                    if(dao.getRsdados().first()){
                        ExibeRegistro(dao.getSalvamentos());
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(JanelaSalvamentos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btn_abrebancoActionPerformed

    private void btn_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirActionPerformed
       if(bancoEstaAberto(bancoLigado) == false)
            return;
 
            Salvamentos salvamento = CarregaSalvamento();
            if (dao.Inserir(salvamento)) {
                JOptionPane.showMessageDialog(this, "Salvamento inserido com sucesso. Veja o relatorio para melhor visualização");
            }
            else {
            JOptionPane.showMessageDialog(this, "Erro ao inserir Salvamento.\n"
                    + "verifique se já não existe este mesmo heroi, neste mesmo dia já cadastrado\n"
                    + "se ja houver, use a função alterar para somar a quantidade de salvamentos realizados neste dia\n"
                    + "caso contrario, pode ser o banco de dados, verifique o log");
            }

    }//GEN-LAST:event_btn_inserirActionPerformed

