    public void ExibeRegistro(Salvamentos salvamento) {
        jTF_id.setText(String.valueOf(salvamento.getId()));
        jTF_heroi.setText(String.valueOf(salvamento.getHeroi()));
        jTF_qtsalvamento.setText(String.valueOf(salvamento.getQtSalvamento()));
    }

    public Salvamentos CarregaSalvamento() {
        Salvamentos salvamento = null;
        int id = Integer.valueOf(jTF_id.getText());
        int heroi = Integer.valueOf(jTF_heroi.getText());
        int qtSalvamento = Integer.valueOf(jTF_qtsalvamento.getText());
        salvamento = new Salvamentos(id, heroi, qtSalvamento);
        return salvamento;
    }
    
public boolean bancoEstaAberto(boolean banco){
        if(banco == false){
            JOptionPane.showMessageDialog(this, "Ligue o banco de dados primeiro!!");
            return false;
        }
        return true;
}

